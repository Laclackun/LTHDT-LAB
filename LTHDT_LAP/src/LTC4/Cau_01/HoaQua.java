public class HoaQua {

    private double gia;
    private double can;
    private double SL;

    public double getGia() {
        return gia;
    }

    public void setGia(double gia) {
        this.gia = gia;
    }
    
    public double getCan() {
        return can;
    }

    public void setCan(double can) {
        this.can = can;
    }
    
    public double getSL() {
        return SL;
    }

    public void setSL(double SL) {
        this.SL = SL;
    }
    
    public HoaQua(double gia, double can, double SL) {
        this.gia = gia;
        this.can = can;
        this.SL = SL;
    }
    
    
}
    
